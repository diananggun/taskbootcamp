class Main {
    public static void main(String[] args) {
        calculateOperations();
    }

    public static void calculateOperations() {
        // Penjumlahan
        int angka1 = 3;
        int angka2 = 4;
        int hasilPenjumlahan = angka1 + angka2;
        System.out.println("Penjumlahan: " + hasilPenjumlahan);

        // Pengurangan
        int angka3 = 15;
        int angka4 = 4;
        int hasilPengurangan = angka3 - angka4;
        System.out.println("Pengurangan: " + hasilPengurangan);

        // Perkalian
        int angka5 = 10;
        int angka6 = 10;
        int hasilPerkalian = angka5 * angka6;
        System.out.println("Perkalian: " + hasilPerkalian);

        // Pembagian
        int angka7 = 12;
        int angka8 = 3;
        int hasilPembagian = angka7 / angka8;
        System.out.println("Pembagian: " + hasilPembagian);
    }
}
